-- Drop tables if they already exist (for clean re-runs)
DROP TABLE IF EXISTS fact_sales;
DROP TABLE IF EXISTS dim_date;
DROP TABLE IF EXISTS dim_store;
DROP TABLE IF EXISTS dim_product;

-- =====================
-- DIMENSION TABLE: dim_date
-- Stores all date-related attributes for time-series analysis
-- =====================
CREATE TABLE dim_date (
    date_id     VARCHAR(10)  PRIMARY KEY,  -- Surrogate key e.g. DT0001
    full_date   DATE         NOT NULL,     -- Actual date e.g. 2023-01-15
    day         INT          NOT NULL,     -- Day of month (1-31)
    month       INT          NOT NULL,     -- Month number (1-12)
    month_name  VARCHAR(15)  NOT NULL,     -- Month name e.g. January
    quarter     INT          NOT NULL,     -- Quarter (1-4)
    year        INT          NOT NULL      -- Year e.g. 2023
);

-- =====================
-- DIMENSION TABLE: dim_store
-- Stores all store/location attributes
-- =====================
CREATE TABLE dim_store (
    store_id    VARCHAR(10)  PRIMARY KEY,  -- Surrogate key e.g. STR001
    store_name  VARCHAR(100) NOT NULL,     -- Store branch name
    store_city  VARCHAR(50)  NOT NULL      -- City where store is located
);

-- =====================
-- DIMENSION TABLE: dim_product
-- Stores all product attributes
-- =====================
CREATE TABLE dim_product (
    product_id    VARCHAR(10)   PRIMARY KEY,  -- Surrogate key e.g. PRD001
    product_name  VARCHAR(100)  NOT NULL,     -- Product name e.g. Phone
    category      VARCHAR(50)   NOT NULL,     -- Standardized category
    unit_price    DECIMAL(12,2) NOT NULL      -- Price per unit in INR
);

-- =====================
-- FACT TABLE: fact_sales
-- Central table containing all measurable business events (transactions)
-- Foreign keys link to all 3 dimension tables
-- =====================
CREATE TABLE fact_sales (
    transaction_id  VARCHAR(20)   PRIMARY KEY,  -- Original transaction ID
    date_id         VARCHAR(10)   NOT NULL,     -- FK → dim_date
    store_id        VARCHAR(10)   NOT NULL,     -- FK → dim_store
    product_id      VARCHAR(10)   NOT NULL,     -- FK → dim_product
    customer_id     VARCHAR(20)   NOT NULL,     -- Customer reference
    units_sold      INT           NOT NULL,     -- Quantity sold
    unit_price      DECIMAL(12,2) NOT NULL,     -- Price at time of sale
    total_revenue   DECIMAL(15,2) NOT NULL,     -- units_sold × unit_price
    FOREIGN KEY (date_id)    REFERENCES dim_date(date_id),
    FOREIGN KEY (store_id)   REFERENCES dim_store(store_id),
    FOREIGN KEY (product_id) REFERENCES dim_product(product_id)
);

